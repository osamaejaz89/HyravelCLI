class Drivers {
  constructor(
    id,
    cnic,
    contact_number,
    driving_licence,
    email,
    name,
    password,
    status,
    username
  ) {
    this.id = id;
    this.cnic = cnic;
    this.contact_number = contact_number;
    this.driving_licence = driving_licence;
    this.email = email;
    this.name = name;
    this.password = password;
    this.status = status;
    this.username = username;
  }
}

export default Drivers;
