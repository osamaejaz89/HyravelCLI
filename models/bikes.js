class Bike {
    constructor(
        Bike_Brand,
        Bike_ID,
        Bike_category, 
        Bike_charges,
        Bike_model_year, 
        Bike_name,
        Bike_plate_number,
        url
    ) {
      this.Bike_Brand = Bike_Brand;
      this.Bike_ID = Bike_ID;
      this.Bike_category = Bike_category;
      this.Bike_charges = Bike_charges;
      this.Bike_model_year = Bike_model_year;
      this.Bike_name = Bike_name;
      this.Bike_plate_number = Bike_plate_number;
      this.url = url;
    }
  }
  
  export default Bike;
  